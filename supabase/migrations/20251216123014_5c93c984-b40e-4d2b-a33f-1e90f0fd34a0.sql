-- 1. Tabela de categorias financeiras
CREATE TABLE categorias_financeiras (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  codigo TEXT UNIQUE NOT NULL,
  nome TEXT NOT NULL,
  descricao TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 2. Tabela de tipos de transação
CREATE TABLE tipos_transacao (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  codigo TEXT UNIQUE NOT NULL,
  nome TEXT NOT NULL,
  descricao TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 3. Tabela de subcategorias financeiras
CREATE TABLE subcategorias_financeiras (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  codigo TEXT UNIQUE NOT NULL,
  nome TEXT NOT NULL,
  categoria_codigo TEXT REFERENCES categorias_financeiras(codigo),
  descricao TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 4. Tabela principal de transações financeiras
CREATE TABLE transacoes_financeiras (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  mes INTEGER NOT NULL CHECK (mes >= 1 AND mes <= 12),
  ano INTEGER NOT NULL,
  mes_nome TEXT,
  tipo_codigo TEXT REFERENCES tipos_transacao(codigo),
  categoria_codigo TEXT REFERENCES categorias_financeiras(codigo),
  subcategoria_codigo TEXT REFERENCES subcategorias_financeiras(codigo),
  descricao TEXT,
  data_transacao TEXT,
  valor DECIMAL(15,2) NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS
ALTER TABLE categorias_financeiras ENABLE ROW LEVEL SECURITY;
ALTER TABLE tipos_transacao ENABLE ROW LEVEL SECURITY;
